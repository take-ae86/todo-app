    '2-11': '建国記念',
    '2-23': '天皇誕生日',
    '2-24': '振替休日',
    '3-20': '春分の日',
    '4-29': '昭和の日',
    '5-3': '憲法記念日',
    '5-4': 'みどりの日',
    '5-5': 'こどもの日',
    '5-6': '振替休日',
    '7-20': '海の日',
    '8-11': '山の日',
    '9-21': '敬老の日',
    '9-22': '国民の休日',
    '9-23': '秋分の日',
    '10-12': 'スポーツの日',
    '11-3': '文化の日',
    '11-23': '勤労感謝',
    '11-24': '振替休日',
  };
  return holidays['$m-$d'];
}
